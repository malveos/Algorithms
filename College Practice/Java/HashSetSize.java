import java.util.*;
class HashSetSize
{
	public static void main(String[] args) {
		HashSet h= new HashSet(2);
		h.add('a');
		System.out.println("Size:"+h.size());
		h.add('7');
		h.add(99);
		System.out.println("Elements:"+h);
		System.out.println("Elements:"+h);
		System.out.println("Elements:"+h);
		System.out.println("Elements:"+h);
		System.out.println("Size:"+h.size());
	}
}